import torch
import torch.nn as nn
import torch.nn.functional as F
from torch.autograd import Variable

X_Train = Variable(torch.from_numpy(x_train), requires_grad=True)
Y_Train = Variable(torch.from_numpy(y_train))

# print(X_Train[0].view(-1, 1))


class bp_nn(nn.Module):
    def __init__(self):
        super(bp_nn, self).__init__()
        self.hidden = nn.Linear(63, 6)
        self.out = nn.Linear(6, 10)

    def forward(self, x):
        hidden = self.hidden(x)
        hidden = F.sigmoid(hidden)
        out = self.out(hidden)
        out = F.sigmoid(out)
        return out


BP = bp_nn()

optim = torch.optim.Adam(BP.parameters(), lr=0.1)
loss_func = nn.MSELoss()

for epoch in range(600):
    x_input = X_Train.float()
    y = Y_Train.float()

    out = BP(x_input)
    loss = loss_func(out, y)
    optim.zero_grad()
    loss.backward()
    optim.step()

    print(loss)
